package com.orbit.benchmarks;

import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Level;
import org.openjdk.jmh.annotations.Measurement;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.annotations.OutputTimeUnit;
import org.openjdk.jmh.annotations.Param;
import org.openjdk.jmh.annotations.Scope;
import org.openjdk.jmh.annotations.Setup;
import org.openjdk.jmh.annotations.State;
import org.openjdk.jmh.annotations.Warmup;
import org.openjdk.jmh.infra.Blackhole;

import java.util.concurrent.TimeUnit;

/**
 * Benchmark: Orbit's Perl escape sequences ({@code \h}, {@code \v}, {@code \H}, {@code \V},
 * {@code \R}, {@code \N}, braced hex {@code \x{NNN}}) vs their manual character-class
 * equivalents and, where supported, the JDK {@code java.util.regex} equivalent.
 *
 * <p>Each scenario benchmarks three paths:
 * <ol>
 *   <li>{@code orbitEscape}   — Orbit pattern using the Perl escape shorthand</li>
 *   <li>{@code orbitEquiv}    — Orbit pattern using the manually expanded character class</li>
 *   <li>{@code jdkEquiv}      — JDK pattern (skipped silently when JDK has no equivalent)</li>
 * </ol>
 *
 * <p>Scenarios with no JDK equivalent: {@code R_escape}, {@code R_corpus}, {@code N_escape},
 * {@code braced_hex_high}.  In these cases {@code jdkMatcher} is {@code null} and
 * {@code jdkEquiv} consumes {@code 0} and returns immediately.
 *
 * <p>Corpus scenarios ({@code v_corpus}, {@code R_corpus}) run against an Apache-style
 * access-log corpus (~1 MB) generated by {@link CorpusGenerator#accessLog(int)}.
 *
 * <p>Instances are <em>not</em> thread-safe. The benchmark is run with {@code Scope.Benchmark},
 * which provides one instance per benchmark fork.
 *
 * <p>Run:
 * <pre>{@code
 *   java --enable-preview -jar orbit-benchmarks.jar EscapesBenchmark
 * }</pre>
 */
@State(Scope.Benchmark)
@BenchmarkMode(Mode.AverageTime)
@OutputTimeUnit(TimeUnit.NANOSECONDS)
@Warmup(iterations = 5, time = 1)
@Measurement(iterations = 10, time = 1)
@Fork(value = 1, jvmArgsPrepend = {"--enable-preview", "--add-modules", "jdk.incubator.vector"})
public class EscapesBenchmark {

  @Param({
    "h_escape",
    "v_escape",
    "H_escape",
    "V_escape",
    "R_escape",
    "N_escape",
    "braced_hex",
    "braced_hex_high",
    "h_in_class",
    "v_corpus",
    "R_corpus"
  })
  private String scenario;

  private String input;

  /** Orbit pattern using the Perl escape shorthand. */
  private com.orbit.api.Matcher orbitMatcher;

  /** Orbit pattern using the manually expanded character-class equivalent. */
  private com.orbit.api.Matcher orbitEquivMatcher;

  /**
   * JDK pattern where JDK supports an equivalent; {@code null} for scenarios where JDK has no
   * equivalent ({@code R_escape}, {@code R_corpus}, {@code N_escape}, {@code braced_hex_high}).
   */
  private java.util.regex.Matcher jdkMatcher;

  /**
   * Compiles both Orbit patterns and, where applicable, the JDK pattern for the current
   * {@code scenario}.  Corpus inputs are built once here.
   */
  @Setup(Level.Trial)
  public void setup() {
    String orbitEscapePattern;
    String orbitEquivPatternStr;
    String jdkPatternStr; // null means JDK has no equivalent for this scenario

    switch (scenario) {
      case "h_escape" -> {
        orbitEscapePattern = "\\h+";
        orbitEquivPatternStr =
            "[ \\t\\u00A0\\u1680\\u2000-\\u200A\\u202F\\u205F\\u3000]+";
        jdkPatternStr = "[ \\t\\u00A0\\u1680\\u2000-\\u200A\\u202F\\u205F\\u3000]+";
        input = "hello\tworld \u00A0non-breaking \u3000ideographic";
      }
      case "v_escape" -> {
        orbitEscapePattern = "\\v+";
        orbitEquivPatternStr = "[\\n\\u000B\\f\\r\\u0085\\u2028\\u2029]+";
        jdkPatternStr = "[\\n\\u000B\\f\\r\\u0085\\u2028\\u2029]+";
        input = "line1\nline2\rline3\u000Bline4\u0085line5";
      }
      case "H_escape" -> {
        orbitEscapePattern = "\\H+";
        orbitEquivPatternStr =
            "[^ \\t\\u00A0\\u1680\\u2000-\\u200A\\u202F\\u205F\\u3000]+";
        jdkPatternStr = "[^ \\t\\u00A0\\u1680\\u2000-\\u200A\\u202F\\u205F\\u3000]+";
        input = "hello world\tfoo";
      }
      case "V_escape" -> {
        orbitEscapePattern = "\\V+";
        orbitEquivPatternStr = "[^\\n\\u000B\\f\\r\\u0085\\u2028\\u2029]+";
        jdkPatternStr = "[^\\n\\u000B\\f\\r\\u0085\\u2028\\u2029]+";
        input = "hello\nworld\rfoo";
      }
      case "R_escape" -> {
        // JDK does not support \R — jdkPatternStr is null
        orbitEscapePattern = "\\R";
        orbitEquivPatternStr = "\\r\\n|[\\r\\n\\u000B\\f\\u0085\\u2028\\u2029]";
        jdkPatternStr = null;
        input = "line1\r\nline2\nline3\rline4\u0085line5";
      }
      case "N_escape" -> {
        // JDK does not support \N — jdkPatternStr is null
        orbitEscapePattern = "\\N+";
        orbitEquivPatternStr = "[^\\n]+";
        jdkPatternStr = null;
        input = "hello world\nfoo bar\nbaz";
      }
      case "braced_hex" -> {
        orbitEscapePattern = "\\x{41}\\x{42}\\x{43}";
        orbitEquivPatternStr = "ABC";
        jdkPatternStr = "\\x{41}\\x{42}\\x{43}";
        input = "ABC DEF XYZ ABC";
      }
      case "braced_hex_high" -> {
        // JDK uses Unicode escape notation; \x{NNN} range syntax is Orbit/Perl-specific
        orbitEscapePattern = "[\\x{100}-\\x{17F}]+";
        orbitEquivPatternStr = "[\\u0100-\\u017F]+";
        jdkPatternStr = null;
        input = "\u0100\u0101\u0102 abc \u017E\u017F";
      }
      case "h_in_class" -> {
        orbitEscapePattern = "[\\h\\d]+";
        orbitEquivPatternStr =
            "[ \\t\\u00A0\\u1680\\u2000-\\u200A\\u202F\\u205F\\u30000-9]+";
        jdkPatternStr = "[ \\t\\u00A0\\u1680\\u2000-\\u200A\\u202F\\u205F\\u30000-9]+";
        input = "hello 123\tworld 456";
      }
      case "v_corpus" -> {
        orbitEscapePattern = "\\v";
        orbitEquivPatternStr = "[\\n\\u000B\\f\\r\\u0085\\u2028\\u2029]";
        jdkPatternStr = "[\\n\\u000B\\f\\r\\u0085\\u2028\\u2029]";
        input = CorpusGenerator.accessLog(8_000);
      }
      case "R_corpus" -> {
        // JDK does not support \R — jdkPatternStr is null
        orbitEscapePattern = "\\R";
        orbitEquivPatternStr = "\\r\\n|[\\r\\n\\u000B\\f\\u0085\\u2028\\u2029]";
        jdkPatternStr = null;
        input = CorpusGenerator.accessLog(8_000);
      }
      default -> throw new IllegalArgumentException("Unknown scenario: " + scenario);
    }

    orbitMatcher =
        com.orbit.api.Pattern.compile(orbitEscapePattern).matcher(input);
    orbitEquivMatcher =
        com.orbit.api.Pattern.compile(orbitEquivPatternStr).matcher(input);
    jdkMatcher =
        jdkPatternStr != null
            ? java.util.regex.Pattern.compile(jdkPatternStr).matcher(input)
            : null;
  }

  /**
   * Orbit: find all matches using the Perl escape shorthand pattern, consuming each start
   * position.
   *
   * @param bh JMH blackhole that prevents dead-code elimination
   * @return number of matches found
   */
  @Benchmark
  public int orbitEscape(Blackhole bh) {
    orbitMatcher.reset();
    int count = 0;
    while (orbitMatcher.find()) {
      bh.consume(orbitMatcher.start());
      count++;
    }
    return count;
  }

  /**
   * Orbit: find all matches using the manually expanded character-class equivalent, consuming
   * each start position.
   *
   * @param bh JMH blackhole that prevents dead-code elimination
   * @return number of matches found
   */
  @Benchmark
  public int orbitEquiv(Blackhole bh) {
    orbitEquivMatcher.reset();
    int count = 0;
    while (orbitEquivMatcher.find()) {
      bh.consume(orbitEquivMatcher.start());
      count++;
    }
    return count;
  }

  /**
   * JDK {@code java.util.regex}: find all matches using the JDK-equivalent pattern.
   *
   * <p>When the current scenario has no JDK equivalent ({@code R_escape}, {@code R_corpus},
   * {@code N_escape}, {@code braced_hex_high}), this method consumes {@code 0} and returns
   * immediately so that the scenario still appears in the results without inflating timings.
   *
   * @param bh JMH blackhole that prevents dead-code elimination
   * @return number of matches found, or {@code 0} when JDK has no equivalent
   */
  @Benchmark
  public int jdkEquiv(Blackhole bh) {
    if (jdkMatcher == null) {
      bh.consume(0);
      return 0;
    }
    jdkMatcher.reset();
    int count = 0;
    while (jdkMatcher.find()) {
      bh.consume(jdkMatcher.start());
      count++;
    }
    return count;
  }
}
